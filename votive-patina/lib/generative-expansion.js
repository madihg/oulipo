/**
 * lib/generative-expansion.js
 * votivepatina - optional, OFF-by-default generative layer.
 *
 * SECURITY NOTE: Passing an Anthropic API key directly to config.apiKey in a
 * browser context exposes it to anyone who opens DevTools. This code path is
 * for LOCAL ARTIST TESTING ONLY. Real deployment must route through a
 * server-side proxy that holds the key (set config.endpoint to your proxy URL
 * and omit config.apiKey).
 *
 * This module is NEVER imported by index.html or main.js. It exists solely
 * for experimental/generative.html.
 */

// --------------------------------------------------------------------------
// Feature flag - hard off. The shipped artwork is fully offline.
// --------------------------------------------------------------------------

export const GENERATIVE_ENABLED = false;

// --------------------------------------------------------------------------
// Default model - current production Claude Sonnet.
// --------------------------------------------------------------------------

const DEFAULT_MODEL = "claude-sonnet-4-6";

// --------------------------------------------------------------------------
// System-prompt builder
//
// Receives the authored expansions as exemplars and constructs a system block
// that establishes Halim's voice. The exemplars block is marked with
// cache_control so the Anthropic API can cache it across calls - the voice
// description and all five examples stay warm; only the per-line user turn
// changes.
// --------------------------------------------------------------------------

function buildSystemBlock(exemplars) {
  const exemplarText = exemplars
    .map(
      (ex, i) =>
        `Prayer line ${i + 1}:\n` +
        `  Arabic: ${ex.arabic}\n` +
        `  Literal: ${ex.literal}\n` +
        `  Authored expansion:\n  ${ex.expansion}`,
    )
    .join("\n\n");

  return {
    type: "text",
    text: `You are dreaming a devotional expansion for a net.art prayer piece called votivepatina.

The artist, Halim, has written five intimate, surreal expansions in their own voice. Study them carefully - they are the voice the machine is attempting to dream toward.

Voice characteristics drawn from the authored work:
- Intimate and bodily: the sacred and the flesh are one substance
- Surreal juxtapositions: liturgy and pop culture, ancient and contemporary, Arabic and English
- Devotional but destabilising: prayer as something that might break or bleed
- Second-person direct address to Mary ("Mary, ...") used sparingly but precisely
- Unexpected pivots that detonate meaning rather than build toward it
- Short, sharp declarative sentences mixed with longer accumulative ones
- The body is always present: sweat, skin, bruises, blood, mouths, hands
- Humor and violence can sit in the same sentence without cancelling each other

${exemplarText}

You are not Halim. You are a machine dreaming toward that voice - similar texture, different specific images. The machine's version will always be marked as machine-generated by the interface that calls you.

Write ONE short expansion (maximum 90 words) for the prayer line given by the user. Return ONLY the expansion text - no preamble, no quotation marks, no explanation.`,
    // Cache the system block (voice brief + all five exemplars) across calls.
    // Only the per-line user turn differs between the five possible requests.
    cache_control: { type: "ephemeral" },
  };
}

// --------------------------------------------------------------------------
// dreamExpansion
//
// Parameters:
//   line      - { arabic: string, literal: string } the line to expand.
//   exemplars - array of { arabic, literal, expansion } (all five authored
//               expansions). Pass content.json lines[*] directly.
//   config    - {
//                 endpoint?: string,   // proxy URL OR https://api.anthropic.com
//                 apiKey?:   string,   // direct key (local testing ONLY)
//                 model?:    string,   // defaults to DEFAULT_MODEL
//                 force?:    boolean,  // bypass GENERATIVE_ENABLED guard
//               }
//   signal    - optional AbortSignal for cancellation.
//
// Returns a Promise<string> - the dreamt expansion text.
// Throws if GENERATIVE_ENABLED is false and config.force !== true.
// --------------------------------------------------------------------------

export async function dreamExpansion({
  line,
  exemplars,
  config = {},
  signal,
} = {}) {
  // Guard: refuse if the feature is off, unless the caller explicitly opts in.
  if (!GENERATIVE_ENABLED && config.force !== true) {
    throw new Error(
      "dreamExpansion: GENERATIVE_ENABLED is false. " +
        "Pass config.force = true to call this from an experimental context.",
    );
  }

  if (
    !line ||
    typeof line.arabic !== "string" ||
    typeof line.literal !== "string"
  ) {
    throw new Error(
      "dreamExpansion: `line` must have { arabic, literal } string fields.",
    );
  }

  if (!Array.isArray(exemplars) || exemplars.length === 0) {
    throw new Error(
      "dreamExpansion: `exemplars` must be a non-empty array of authored expansions.",
    );
  }

  const model = config.model || DEFAULT_MODEL;

  // Resolve where to POST. A proxy URL takes precedence over direct Anthropic.
  // The proxy should accept the same request shape and forward it server-side
  // with the real API key - the key never travels to the browser that way.
  const endpoint = config.endpoint || "https://api.anthropic.com/v1/messages";

  const headers = {
    "Content-Type": "application/json",
    "anthropic-version": "2023-06-01",
    // Prompt caching requires this beta header.
    "anthropic-beta": "prompt-caching-2024-07-31",
  };

  // Only attach the Authorization header when calling the Anthropic API
  // directly (local testing). A proxy endpoint handles auth server-side.
  if (config.apiKey) {
    headers["x-api-key"] = config.apiKey;
  }

  const systemBlock = buildSystemBlock(exemplars);

  const body = {
    model,
    max_tokens: 256,
    system: [systemBlock],
    messages: [
      {
        role: "user",
        content:
          `Prayer line to dream an expansion for:\n` +
          `  Arabic: ${line.arabic}\n` +
          `  Literal: ${line.literal}\n\n` +
          `Write one short expansion (maximum 90 words) in Halim's dreamed voice. Return only the text.`,
      },
    ],
  };

  const response = await fetch(endpoint, {
    method: "POST",
    headers,
    body: JSON.stringify(body),
    signal,
  });

  if (!response.ok) {
    let detail = "";
    try {
      const err = await response.json();
      detail = err.error?.message || JSON.stringify(err);
    } catch (_) {
      detail = await response.text().catch(() => "(unreadable response)");
    }
    throw new Error(`dreamExpansion: API error ${response.status} - ${detail}`);
  }

  const data = await response.json();

  // Anthropic Messages API: content[0].text for a text block.
  const text = data?.content?.[0]?.text;
  if (typeof text !== "string" || text.trim() === "") {
    throw new Error(
      "dreamExpansion: unexpected response shape - no text content found.",
    );
  }

  return text.trim();
}
